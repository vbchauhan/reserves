    <div style="color:#990000; font-size:14px"><strong>Reserve Policies:</strong></div>
    
    <ol> 
      <li>In accordance with Title 17, United States Code, United States Copyright Law, all copied, copyrighted items placed on 	reserve require copyright permission, with the exception of first time or one time use. Items can be placed on reserve without copyright permission ONLY if they are being used for the first time or only for one time. Subsequent 	use requires copyright permission.
        </li> 
      
      <li>If such a repeat reserve item is not available in the library, on the Web, or through UMCP Web databases, the faculty member must request copyright permission for one print copy for one semester or year. Faculty must bring in a letter with copyright permission granted, or the library will have to take the item down.
        </li> 
      
      <li>All reserve materials must contain on the face of the material the statement - NOTICE: This item may be protected by Copyright Law (TITLE 17 UNITED STATES CODE).
        </li> 
      
      <li>The library will take all precautions necessary to keep reserve materials secure and in good condition. However, the library cannot be held liable for stolen or damaged reserve items.
        </li> 
      
      <li>Reserve materials are processed in the order in which they are received, with priority given to materials for the current semester.
        </li> 
      
      <li>Faculty are required to retrieve reserve materials no later than two weeks after the end of the semester for courses	that are taught once a year or less frequently.
        </li> 
      
